void display_page() {

	switch(page) {
	case firstPage:
		show_first_page();
		break;
	case secondPage:
		show_pass_page();
		break;
	case thirdPage:
		show_safe_page();
		break;
	}
}

void change_page() {
	page += 1;
}

void set_volume(int volume) {
	pData.volume = volume;
}

void apply_volume() {
	if (pData.first_digit == -1) {
		pData.first_digit = pData.volume;
	}
	else if (pData.second_digit == -1) {
		pData.second_digit = pData.volume;
	}
	else if (pData.third_digit == -1) {
		pData.third_digit = pData.volume;
	}
	else if (pData.fourth_digit == -1) {
		pData.fourth_digit = pData.volume;
	}
}

bool is_password_choosed() {
	if((pData.first_digit != -1) && (pData.second_digit != -1) && (pData.third_digit != -1) && (pData.fourth_digit != -1)) {
		return true;
	} else {
		return false;
	}
}

void validate_password() {
	if(
			correctPassword.first_digit == pData.first_digit &&
			correctPassword.second_digit == pData.second_digit &&
			correctPassword.third_digit == pData.third_digit &&
			correctPassword.fourth_digit == pData.fourth_digit
	) {
		clear();
		change_page();
		display_page();
	} else {
		int last_volume = pData.volume;
		init_Data();
		pData.volume = last_volume;
		pData.wrong_pass = true;
		display_page();
		buzzerChangeTone(2000, 50);
		HAL_Delay(300);
		buzzerChangeTone(0, 0);
		setCursor(5, 3);
		write(0);
		write(0);
		write(0);
	}
}

